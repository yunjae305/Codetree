A, B = map(int, input().split())

# 1. A가 B보다 같거나 큰지
if A >= B:
    print(1)
else:
    print(0)

# 2. A가 B보다 큰지
if A > B:
    print(1)
else:
    print(0)

# 3. B가 A보다 같거나 큰지
if B >= A:
    print(1)
else:
    print(0)

# 4. B가 A보다 큰지
if B > A:
    print(1)
else:
    print(0)

# 5. A와 B가 같은지
if A == B:
    print(1)
else:
    print(0)

# 6. A와 B가 다른지
if A != B:
    print(1)
else:
    print(0)